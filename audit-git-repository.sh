#!/bin/bash

echo "=========================================================="
echo "           GIT REPOSITORY AUDIT"
echo "=========================================================="

echo
echo "Current Directory"
pwd

echo
echo "Git Version"
git --version

echo
echo "----------------------------------------------------------"
echo "Repository Size"
echo "----------------------------------------------------------"

du -sh .

echo
git count-objects -vH

echo
echo "----------------------------------------------------------"
echo "Tracked node_modules"
echo "----------------------------------------------------------"

git ls-files | grep node_modules || echo "OK"

echo
echo "----------------------------------------------------------"
echo "Tracked .next"
echo "----------------------------------------------------------"

git ls-files | grep "^.next" || echo "OK"

echo
echo "----------------------------------------------------------"
echo "Tracked dist"
echo "----------------------------------------------------------"

git ls-files | grep "^dist" || echo "OK"

echo
echo "----------------------------------------------------------"
echo "Tracked build"
echo "----------------------------------------------------------"

git ls-files | grep "^build" || echo "OK"

echo
echo "----------------------------------------------------------"
echo "Tracked coverage"
echo "----------------------------------------------------------"

git ls-files | grep "^coverage" || echo "OK"

echo
echo "----------------------------------------------------------"
echo "Tracked Logs"
echo "----------------------------------------------------------"

git ls-files | grep logs || echo "OK"

echo
echo "----------------------------------------------------------"
echo "Tracked Database"
echo "----------------------------------------------------------"

git ls-files | grep -E "\.sqlite|\.db|\.sqlitedb" || echo "OK"

echo
echo "----------------------------------------------------------"
echo "Tracked Environment"
echo "----------------------------------------------------------"

git ls-files | grep ".env" || echo "OK"

echo
echo "----------------------------------------------------------"
echo "Large Files (>50MB)"
echo "----------------------------------------------------------"

find . -type f -size +50M

echo
echo "----------------------------------------------------------"
echo "Largest 50 Files"
echo "----------------------------------------------------------"

find . -type f -exec du -h {} + | sort -hr | head -50

echo
echo "----------------------------------------------------------"
echo "Git Ignore"
echo "----------------------------------------------------------"

if [ -f .gitignore ]; then
    cat .gitignore
else
    echo ".gitignore NOT FOUND"
fi

echo
echo "----------------------------------------------------------"
echo "Current Status"
echo "----------------------------------------------------------"

git status

echo
echo "----------------------------------------------------------"
echo "Remote"
echo "----------------------------------------------------------"

git remote -v

echo
echo "----------------------------------------------------------"
echo "Branches"
echo "----------------------------------------------------------"

git branch -a

echo
echo "----------------------------------------------------------"
echo "Recent Commits"
echo "----------------------------------------------------------"

git log --oneline -10

echo
echo "----------------------------------------------------------"
echo "Files > 100MB in Git History"
echo "----------------------------------------------------------"

git rev-list --objects --all |
git cat-file --batch-check='%(objectname) %(objecttype) %(objectsize) %(rest)' |
awk '$2=="blob" {print $3"\t"$4}' |
sort -n |
tail -20



echo
echo "----------------------------------------------------------"
echo "Generate Enterprise .gitignore"
echo "----------------------------------------------------------"

# Backup existing .gitignore
if [ -f ".gitignore" ]; then
    cp .gitignore .gitignore.backup.$(date +%Y%m%d_%H%M%S)
    echo "Backup created."
else
    touch .gitignore
    echo ".gitignore created."
fi

RULES=$(cat <<'EOF'

###############################################################################
# Enterprise Node.js
###############################################################################

node_modules/
npm-debug.log*
yarn-debug.log*
pnpm-debug.log*

###############################################################################
# Next.js
###############################################################################

.next/
out/

###############################################################################
# Build
###############################################################################

dist/
build/
coverage/

###############################################################################
# Cache
###############################################################################

.cache/
.turbo/
.eslintcache
*.tsbuildinfo

###############################################################################
# Environment
###############################################################################

.env
.env.*
!.env.example

###############################################################################
# Logs
###############################################################################

logs/
*.log

###############################################################################
# Docker
###############################################################################

docker-data/
postgres-data/
redis-data/
*.pid

###############################################################################
# PostgreSQL
###############################################################################

*.sql.gz
*.dump
*.backup

###############################################################################
# SQLite
###############################################################################

*.sqlite
*.sqlite3
*.db

###############################################################################
# Uploads
###############################################################################

uploads/
storage/

###############################################################################
# IDE
###############################################################################

.vscode/
.idea/

###############################################################################
# macOS
###############################################################################

.DS_Store

###############################################################################
# Linux
###############################################################################

*~

###############################################################################
# Temporary
###############################################################################

tmp/
temp/

###############################################################################
# Test
###############################################################################

.nyc_output/

###############################################################################
# Misc
###############################################################################

*.swp
*.swo

EOF
)

while IFS= read -r line
do
    [ -z "$line" ] && continue

    grep -qxF "$line" .gitignore || echo "$line" >> .gitignore

done <<< "$RULES"

echo
echo ".gitignore updated successfully."

echo
echo "----------------------------------------------------------"
echo "Verify Important Ignore Rules"
echo "----------------------------------------------------------"

for rule in \
"node_modules/" \
".next/" \
"dist/" \
"build/" \
"coverage/" \
".env" \
".env.*" \
"logs/" \
".DS_Store"
do
    if grep -qxF "$rule" .gitignore
    then
        printf "[OK] %s\n" "$rule"
    else
        printf "[MISSING] %s\n" "$rule"
    fi
done

echo
echo "=========================================================="
echo "AUDIT FINISHED"
echo "=========================================================="
